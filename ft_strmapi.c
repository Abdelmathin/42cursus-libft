#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*s2;
	unsigned int	pos;

	if (!s || !f)
		return (0);
	s2 = ft_strdup(s);
	pos = 0;
	while (s2 && s[pos])
	{
		s2[pos] = f(pos, s[pos]);
		pos++;
	}
	return (s2);
}
