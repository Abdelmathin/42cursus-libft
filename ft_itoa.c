#include "libft.h"

char	*ft_itoa(int n)
{
	int		i;
	char	dst[12];

	dst[11] = 0;
	i = 10;
	while (n || (i == 10))
	{
		dst[i--] = "9876543210123456789"[9 + n % 10];
		if (-10 < n && n < 0)
			dst[i--] = '-';
		n = n / 10;
	}
	return (ft_strdup(dst + i + 1));
}
